  AppDatabase([QueryExecutor? executor])
      : super(executor ?? driftDatabase(name: 'gentleman_os'));

  @override
  int get schemaVersion => 7;

  @override
  MigrationStrategy get migration => MigrationStrategy(
        onCreate: (m) async {
          await m.createAll();
          await _seedInitialData();
        },
        onUpgrade: (m, from, to) async {
          if (from < 2) {
            await _seedKnowledgeArticlesV2();
          }
          if (from < 3) {
            await m.addColumn(knowledgeArticles, knowledgeArticles.readAt);
          }
          if (from < 4) {
            await m.createTable(healthMarkers);
          }
          if (from < 5) {
            await _seedHealthKnowledgeArticles();
          }
          if (from < 6) {
            await _seedHealthHabits();
          }
          if (from < 7) {
            // Пересев базовых статей: правит категорию 'discipline-habits'
            // (была 9=reading, стало 8=discipline). insertOnConflictUpdate —
            // идемпотентно обновляет существующие строки.
            await _seedKnowledgeArticles();
          }
        },
      );

  Future<void> _seedInitialData() async {
    await _seedUserProfile();
    await _seedAchievements();
    await _seedKnowledgeArticles();
    await _seedHealthKnowledgeArticles();
    await _seedHealthHabits();
  }

  Future<void> _seedUserProfile() async {
    await into(userProfile).insert(
      UserProfileCompanion.insert(
        id: const Value(0),
        updatedAt: DateTime.now(),
      ),
    );
  }

  Future<void> _seedAchievements() async {
    // Источник правды — [Achv.all]; те же коды использует AchievementService.
    for (final (code, title, desc) in Achv.all) {
      await into(achievements).insert(
        AchievementsCompanion.insert(
          id: code,
          code: code,
          title: title,
          description: desc,
        ),
      );
    }
  }

